                <img v-if="ruleForm.minsutupian.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.minsutupian.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.minsutupian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="评价时间" prop="pingjiashijian">
              <el-date-picker
				  :disabled=" false  ||ro.pingjiashijian"
                  value-format="yyyy-MM-dd HH:mm:ss"
